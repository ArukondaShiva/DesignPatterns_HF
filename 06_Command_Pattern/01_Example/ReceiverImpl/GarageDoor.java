package ReceiverImpl;

public class GarageDoor {
    
    public void up(){
        System.out.println("Calling door up");
    }

    public void down(){
        System.out.println("Calling door down");
    }

    public void stop(){
        System.out.println("calling door to stop");
    }

    public void lightOn(){
        System.out.println("GarageDoor Light On");
    }

    public void lightOff(){
        System.out.println("GarageDoor Light Off");
    }

    public void openDoor(){
        System.out.println("Garage Door Opened");
    }

}
